package com.publicept.keywordsandexpressions;

public class Main {

	public static void main(String[] args) {

		int myVariable = 50;                    // The entire line is called a complete valid Java statement.
		myVariable++;                            // Complete Java statement
		myVariable--;                            // Complete Java statement
		System.out.println("This is a test.");    // Complete Java statement

		System.out.println("This is" +
				" another" +
				" and still more.");            // Also a complete Java statement

		int antotheValue = 50;
		antotheValue--;    // correct but not recommended!!
	}
}
